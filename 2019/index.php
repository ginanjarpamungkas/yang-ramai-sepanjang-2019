                <p>Banyak peristiwa menonjol sepanjang 2019 yang lalu lalang di meja redaksi Tempo.co. Ada pemilihan kabinet Presiden Joko Widodo atau Jokowi jilid II, demonstrasi mahasiswa terkait revisi Undang-undang KPK, peluncuran perdana mobil Esemka, perceraian Abdul Somad atau UAS, serta pemecatan Pangeran Andrew dari tugas-tugas di Kerajaan Inggris.</p>
